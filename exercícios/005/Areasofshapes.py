area_of_square = float(input("What is the length of a side of the square? "))
square = area_of_square * area_of_square
str(square)
print(f"The area of the square is: {square}" )

length_of_rectangle = float(input("What is the length of rectangle? "))
width_of_rectangle = float(input("What is the width of the rectangle? "))
rectangle = length_of_rectangle * width_of_rectangle
str(rectangle)
print(f"The area of the rectangle is: {rectangle}")

radius_of_circle = float(input("What is the radius of the circle? "))
circle = 3.14 * (radius_of_circle ** 2)
str(circle)
print(f"The area of the circle is: {circle}")
